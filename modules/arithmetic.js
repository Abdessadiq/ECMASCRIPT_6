/**
 * Addition
 */
export const add = (x, y) => {
  return parseFloat(x) + parseFloat(y);
};

/**
 * Multiplication
 */
const mul = (x, y) => {
  return parseFloat(x) * parseFloat(y);
};

/**
 * Division
 */
const div = (x, y) => {
  return parseFloat(x) / parseFloat(y);
};

/**
 * Substraction
 */
const sub = (x, y) => {
  return parseFloat(x) - parseFloat(y);
};
